// LLM Provider Configuration
export interface LLMProvider {
  id: string
  name: string
  description: string
  website: string
  category: "free" | "freemium" | "paid"
  features: string[]
  models: LLMModel[]
  authType: "api_key" | "oauth" | "none"
  apiKeyName?: string
  baseUrl?: string
  docsUrl: string
  freeLimit?: string
  icon: string
}

