// Get all free models across providers
export function getFreeModels(): { provider: LLMProvider; model: LLMModel }[] {
  const freeModels: { provider: LLMProvider; model: LLMModel }[] = []
  
  for (const provider of LLM_PROVIDERS) {
    for (const model of provider.models) {
      if (model.free) {
        freeModels.push({ provider, model })
      }
    }
  }
  
  return freeModels
}

// Get providers by category
export function getProvidersByCategory(category: LLMProvider["category"]): LLMProvider[] {
  return LLM_PROVIDERS.filter(p => p.category === category)
}
